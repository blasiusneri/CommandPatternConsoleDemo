package receiver

class GarageDoor {
    
    fun up() {
        println("GarageDoor is open")
    }

    fun down() {
        println("GarageDoor is close")
    }
    
    fun stop() {
        println("GarageDoor is stop")
    }

    fun lightOn() {
        println("GarageDoor light on")
    }

    fun lightOff() {
        println("GarageDoor light off")
    }
}